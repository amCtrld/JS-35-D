//Creating arrays
let fruitsList = ["apple", "banana", "orange"];
console.log(fruitsList);
//Accessing Arrays
console.log(fruitsList[1]);
//Length of array
console.log(fruitsList.length);
//Adding element to last
fruitsList.push("mango");
//Adding Element to first
fruitsList.unshift("kiwi");
console.log(fruitsList);
//Removing last Element
fruitsList.pop();
console.log(fruitsList);
//Removing first element
fruitsList.shift();
console.log(fruitsList);